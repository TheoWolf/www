			<h2>Values</h2>

			<p>Databrary aims to increase openness in scientific research. We seek to enable researchers to share their findings with other scientists and the community at-large while protecting the privacy of individuals who participate in research. Participants and others depicted in recordings have the right to consent to having the recordings shared with Databrary. Their wishes must be respected.</p> 

			<p>Achieving these goals requires that we create and maintain a community that embodies the highest ethical standards.</p>

			<h2>Procedures</h2>

			<p>Databrary is governed by a set of standard operating procedures (SOPs). Individuals who wish to become users or contributors must apply for access. Only individuals who demonstrate knowledge of the principles of research ethics with human participants and who agree to the provisions of Databrary's user and/or contributor agreements will be granted access.</p> 

			<p>For more information about Databrary policies and procedures see the <a href="http://github.com/databrary/policies" target="_blank">github repository</a>.</p>
